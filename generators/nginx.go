// HandleNginx manages all nginx commands
func HandleNginx(command string) {
	switch command {
	case "generate":
		GenerateNginxConfig()
	case "help":
		PrintNginxHelp()
	default:
		fmt.Printf("Unknown command: %s\n", command)
	}
}

// AskDomain asks for the main domain
func AskDomain() string {
	prompt := promptui.Prompt{
		Label:   "Main domain",
		Default: "localhost",
	}

	domain, err := runPromptOrExit(prompt)
	if err != nil {
		fmt.Println("Error:", err)
		return ""
	}

	return domain
}
// AskHTTPS asks if using HTTPS
func AskHTTPS() bool {
	prompt := promptui.Select{
		Label: "Use HTTPS?",
		Items: []string{"Yes", "No"},
	}

	_, result, err := runSelectOrExit(prompt)
	if err != nil {
		fmt.Println("Error:", err)
		return false
	}

	return result == "Yes"
}

// AskGenerateCerts asks if we should generate certs with mkcert
func AskGenerateCerts() bool {
	prompt := promptui.Select{
		Label: "Generate local certificates with mkcert?",
		Items: []string{"Yes", "No"},
	}

	_, result, err := runSelectOrExit(prompt)
	if err != nil {
		fmt.Println("Error:", err)
		return false
	}

	return result == "Yes"
}

// GenerateCerts runs mkcert to produce cert and key for domain into outDir
func GenerateCerts(domain, outDir string) error {
	if _, err := exec.LookPath("mkcert"); err != nil {
		return fmt.Errorf("mkcert not found in PATH: %v", err)
	}

	if err := os.MkdirAll(outDir, 0755); err != nil {
		return fmt.Errorf("cannot create out dir: %v", err)
	}

	// Ensure CA installed
	cmd := exec.Command("mkcert", "-install")
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return fmt.Errorf("mkcert -install failed: %v", err)
	}

	certFile := filepath.Join(outDir, domain+".crt")
	keyFile := filepath.Join(outDir, domain+".key")

	hosts := []string{domain, "localhost", "127.0.0.1"}
	args := append([]string{"-cert-file", certFile, "-key-file", keyFile}, hosts...)
	cmd = exec.Command("mkcert", args...)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		return fmt.Errorf("mkcert generation failed: %v", err)
	}

	return nil
}

// AskNumberOfRoutes asks how many routes to configure
func AskNumberOfRoutes() int {
	prompt := promptui.Prompt{
		Label:   "Number of routes (paths)",
		Default: "1",
		Validate: func(input string) error {
			// Simple validation: check if it's a positive number
			num := 0
			_, err := fmt.Sscanf(input, "%d", &num)
			if err != nil {
				return fmt.Errorf("must be a number")
			}
			if num < 1 {
				return fmt.Errorf("must be at least 1")
			}
			return nil
		},
	}

	result, err := runPromptOrExit(prompt)
	if err != nil {
		fmt.Println("Error:", err)
		return 0
	}

	var num int
	fmt.Sscanf(result, "%d", &num)
	return num
}
// AskRoute asks for details of a single route
func AskRoute(routeNumber int) Route {
	fmt.Printf("\n📌 Route %d:\n", routeNumber)

	// Ask for path
	pathPrompt := promptui.Prompt{
		Label:   "Path (e.g., / or /api)",
		Default: "/",
	}
	path, err := runPromptOrExit(pathPrompt)
	if err != nil {
		fmt.Println("Error:", err)
		return Route{}
	}

	// Ask for type
	typePrompt := promptui.Select{
		Label: "Type",
		Items: []string{"Proxy", "Static files"},
	}
	_, routeType, err := runSelectOrExit(typePrompt)
	if err != nil {
		fmt.Println("Error:", err)
		return Route{}
	}

	route := Route{
		Path: path,
	}

	if routeType == "Proxy" {
		route.Type = "proxy"

		// Ask for proxy destination
		proxyPrompt := promptui.Prompt{
			Label:   "Proxy destination (e.g., localhost:3000)",
			Default: "localhost:3000",
		}
		proxy, err := runPromptOrExit(proxyPrompt)
		if err != nil {
			fmt.Println("Error:", err)
			return Route{}
		}
		route.Proxy = proxy

		// Ask for headers
		headersPrompt := promptui.Select{
			Label: "Add standard headers? (Host, X-Real-IP, etc)",
			Items: []string{"Yes", "No"},
		}
		_, headers, err := runSelectOrExit(headersPrompt)
		if err != nil {
			fmt.Println("Error:", err)
			return Route{}
		}
		route.Headers = headers == "Yes"

		// AÑADE ESTO: Ask for WebSocket support
		wsPrompt := promptui.Select{
			Label: "Add WebSocket support? (Upgrade headers)",
			Items: []string{"Yes", "No"},
		}
		_, ws, err := runSelectOrExit(wsPrompt)
		if err != nil {
			fmt.Println("Error:", err)
			return Route{}
		}
		route.WebSocket = ws == "Yes"

	} else {
		route.Type = "static"

		// Ask for directory
		dirPrompt := promptui.Prompt{
			Label:   "Directory to serve (e.g., /home/user/app/dist)",
			Default: "/var/www/html",
		}
		dir, err := runPromptOrExit(dirPrompt)
		if err != nil {
			fmt.Println("Error:", err)
			return Route{}
		}
		route.Directory = dir
	}

	return route
}

// WriteNginxConfig writes the configuration to a file
func WriteNginxConfig(config NginxConfig, filename string) error {
	// Parse the template
	tmpl, err := template.New("nginx").Parse(nginxTemplate)
	if err != nil {
		return fmt.Errorf("error parsing template: %v", err)
	}

	// Normalize path and ensure parent dirs exist
	filename, err = NormalizeOutputPath(filename)
	if err != nil {
		return err
	}

	// Create the file
	file, err := os.Create(filename)
	if err != nil {
		return fmt.Errorf("error creating file: %v", err)
	}
	defer file.Close()

	// Execute template and write to file
	err = tmpl.Execute(file, config)
	if err != nil {
		return fmt.Errorf("error executing template: %v", err)
	}

	return nil
}

// GenerateNginxConfig manages the entire Nginx interactive flow
func GenerateNginxConfig() {
	fmt.Println("\n🔧 Configuring Nginx...")

	domain := AskDomain()
	useHTTPS := AskHTTPS()

	config := NginxConfig{
		Domain:   domain,
		UseHTTPS: useHTTPS,
		Routes:   []Route{},
		CertPath: "/etc/nginx/certs/" + domain,
	}

	// If HTTPS and user wants, generate certs
	if useHTTPS {
		if AskGenerateCerts() {
			outDir := filepath.Join(".", "certs")
			if err := GenerateCerts(domain, outDir); err != nil {
				fmt.Printf("\n❌ Error generating certs: %v\n", err)
				fmt.Println("Continuing without changing cert paths; ensure certs exist at the nginx host path.")
			} else {
				// Use relative project certs mounted to /etc/nginx/certs in docker-compose example
				config.CertPath = "/etc/nginx/certs/" + domain
				fmt.Printf("\n✅ Certificates created at %s (will be referenced as %s.crt/%s.key)\n", outDir, config.CertPath, config.CertPath)
			}
		}
	}

	// Ask for routes
	fmt.Println("\n📍 Configuring routes...")
	numRoutes := AskNumberOfRoutes()

	for i := 1; i <= numRoutes; i++ {
		route := AskRoute(i)
		config.Routes = append(config.Routes, route)
	}

	// Display captured configuration
	fmt.Printf("\n✅ Configuration captured:\n")
	fmt.Printf("   Domain: %s\n", config.Domain)
	fmt.Printf("   HTTPS: %v\n", config.UseHTTPS)
	fmt.Printf("   CertPath: %s\n", config.CertPath)
	fmt.Printf("   Routes: %d\n\n", len(config.Routes))

	for i, route := range config.Routes {
		fmt.Printf("   Route %d: %s (type: %s)\n", i+1, route.Path, route.Type)
		if route.Type == "proxy" {
			fmt.Printf("      → Proxy to: %s\n", route.Proxy)
			fmt.Printf("      → Headers: %v\n", route.Headers)
			fmt.Printf("      → WebSocket: %v\n", route.WebSocket)
		} else {
			fmt.Printf("      → Serve from: %s\n", route.Directory)
		}
	}

	// Generate the file
	filename := askOutput("default.conf")
	err := WriteNginxConfig(config, filename)
	if err != nil {
		fmt.Printf("\n❌ Error generating config: %v\n", err)
		return
	}

	fmt.Printf("\n✅ Generated: %s\n", filename)
	fmt.Println("\n🎉 Ready to use!")
}
