const nginxTemplate = `# Generated by CONGA
# Domain: {{ .Domain }}
{{- if .UseHTTPS }}
# HTTPS enabled (certs at: {{ .CertPath }})
{{- end }}

{{- if .UseHTTPS }}
server {
    listen 80;
    server_name {{ .Domain }};
    return 301 https://$server_name$request_uri;
}
{{- end }}

server {
    {{- if .UseHTTPS }}
    listen 443 ssl http2;
    {{- else }}
    listen 80;
    {{- end }}
    server_name {{ .Domain }};

    {{- if .UseHTTPS }}
    # SSL Configuration
    ssl_certificate {{ .CertPath }}.crt;
    ssl_certificate_key {{ .CertPath }}.key;
    ssl_protocols TLSv1.2 TLSv1.3;
    ssl_ciphers HIGH:!aNULL:!MD5;
    ssl_prefer_server_ciphers on;
    {{- end }}

    # Logs
    access_log /var/log/nginx/{{ .Domain }}_access.log;
    error_log /var/log/nginx/{{ .Domain }}_error.log;

    {{- range .Routes }}
    {{- if eq .Type "proxy" }}

    location {{ .Path }} {
        proxy_pass http://{{ .Proxy }};
        
        {{- if .Headers }}
        # Standard proxy headers
        proxy_set_header Host $host;
        proxy_set_header X-Real-IP $remote_addr;
        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;
        proxy_set_header X-Forwarded-Proto $scheme;
        {{- end }}

        {{- if .WebSocket }}
        # WebSocket support
        proxy_http_version 1.1;
        proxy_set_header Upgrade $http_upgrade;
        proxy_set_header Connection "upgrade";
        {{- end }}

        # Timeouts
        proxy_connect_timeout 60s;
        proxy_send_timeout 60s;
        proxy_read_timeout 60s;
    }
    {{- else if eq .Type "static" }}

    location {{ .Path }} {
        alias {{ .Directory }};
        try_files $uri $uri/ =404;
        expires 7d;
        add_header Cache-Control "public, immutable";
    }
    {{- end }}
    {{- end }}
}
`

// Route represents a path in Nginx
type Route struct {
	Path      string // "/" or "/api"
	Type      string // "proxy" or "static"
	Proxy     string // "localhost:3000"
	Directory string // "/var/www/html"
	Headers   bool   // Add standard headers
	WebSocket bool   // Support WebSockets (for proxy)
}

// NginxConfig is the complete Nginx configuration
type NginxConfig struct {
	Domain   string
	UseHTTPS bool
	Routes   []Route
	// CertPath is the path (on the nginx host) without suffix; template will append .crt/.key
	CertPath string
}

